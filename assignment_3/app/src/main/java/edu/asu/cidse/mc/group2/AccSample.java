public class AccSample {

    public AccSample(float accx, float accy, float accz) {
        this.accx = accx;
        this.accy = accy;
        this.accz = accz;
    }

    float accx;
    float accy;

    public float getAccx() {
        return accx;
    }

    public void setAccx(float accx) {
        this.accx = accx;
    }

    public float getAccy() {
        return accy;
    }

    public void setAccy(float accy) {
        this.accy = accy;
    }

    public float getAccz() {
        return accz;
    }

    public void setAccz(float accz) {
        this.accz = accz;
    }

    float accz;


}
